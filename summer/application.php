					<div style="font-weight: bold; font-size: 20px;">First Round</div>
					In order to be considered, applicants must fill out the Initial Program Application. Duke administrators will review these applications and select the highest potential Start-Ups to be reviewed by the Entrepreneurial Board in the Final Round.  
				</dd>
				<dd style="line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
					<div style="font-weight: bold; font-size: 20px;">Final Round: Dinner Component</div>
					For the second portion of the Final Round, all groups will be invited to a dinner with the Board. First, the dinner serves as an opportunity for each group to interact with other applicants in a more casual and relaxed environment.  Additionally, the dinner is a final chance for groups to speak with, and establish connections with Board members, who might eventually become their Guides during the program. 
				</dd>
