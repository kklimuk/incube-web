					<div style="font-weight: bold; font-size: 20px;">Final Round: Interview Component</div> 
					Each group that is selected to participate in the Final Round will first have a 30 minute interview with the Entrepreneurial Board. The Board is composed of RTP entrepreneurs and Duke administrators/faculty who will make the final acceptance decisions. In addition, individuals from the Board will transition to Guide roles for the selected Start-Ups during the program. 
					<br/><br/>
					The typical interview will consist of:
