<div class="container">
    <div class="row">
		<div class="span16" style="text-align: left; line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
			The purpose of the Summer Innovation Program is to propel existing Duke Start-Ups to the next stage of operation, with the end-goal of creating successful and high-impact ventures. By providing workspace, mentorship, financial support and an entrepreneurial community, the program ensures Start-Ups have the necessary tools to succeed. The Start-Ups accepted into the program will receive the following benefits: 
		</div>
		<div class="span8" style="text-align: left;">
			<div style="height: 10px; font-size: 0px;"></div>
			<dl>
				<dd style="line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
					<div style="font-weight: bold; font-size: 20px;">Stipend</div>
					Each Start-Up will receive a $5,000 dollar stipend. A certain portion of this stipend (the exact amount will be based on the size of the team) will be used to secure on-campus housing within <a href="http://incube.us">InCube</a>. The remainder of the money will be given as a grant to the Start-Up for food and business expenses. 
				</dd>
				<dd style="line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
					<div style="font-weight: bold; font-size: 20px;">Housing (with Community)</div>
					Every group will be provided housing in <a href="http://incube.us">InCube</a>, Duke's entrepreneurial living community.  In addition, the groups will share InCube's common room, which is outfitted with equipment to facilitate entrepreneurship and collaboration. Since groups will both live together in InCube and work together in DuHatch, a strong sense of community will be fostered through the program. 
				</dd>
				<dd style="line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
					<div style="font-weight: bold; font-size: 20px;">Guide</div>
					Since starting a company is very difficult, the program will provide each Start-Up with a Guide that can give advise based on their personal experiences. Each week, groups will meet with their respective Guides to showcase the progress that they have made and discuss current problems/obstacles they are facing.<a name="app"></a> In addition, the Guides will provide accountability for the Start-Ups to ensure that they achieve their goals over the course of the summer.
				</dd>
			</dl>
        </div>
		<div class="span8" style="text-align: left;">
			<div style="height: 10px; font-size: 0px;"></div>
			<dl>
				<dd style="line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
					<div style="font-weight: bold; font-size: 20px;">Office Space</div>
					In order for each group to have a private space to focus on developing its product, the program will provide office space in <a href="http://www.cerc.duke.edu/duhatch/">DUhatch</a>, Duke's entrepreneurial incubator. In addition to providing each Start-Up their own space, the centralized offices will foster a sense of community between Start-Ups, hopefully leading to an exchange of knowledge and support system. 
				</dd>
				<dd style="line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
					<div style="font-weight: bold; font-size: 20px;">Coaches on Call</div>
					In addition to the Guide, each group will also have access to a variety of Coaches on Call to assist in a variety of additional ways. You can see the list of coaches by <a href="http://www.cerc.duke.edu/duhatch/coachprofiles" target="_blank">clicking here</a>.
				</dd>
				<dd style="line-height: 150%; font-size: 16px; font-family: Georgia; padding-top: 15px;">
					<div style="font-weight: bold; font-size: 20px;">Programming (and Length)</div>
					<a name="app"></a>The program will start on May 17th and end on August 15th, 2012, running for roughly ten weeks. Throughout this time frame, multiple programming events will occur. First, on May 21st, there will be an orientation to kick-off the program, where each group will meet their Guide, get acquainted will all of the other groups in the program, and pitch their own startup. During the program, the events will include bi-weekly dinners with RTP entrepreneurs and weekly meetings with the Start-Ups Guide. Finally, the program will conclude with a Demo Day.
				</dd>
			</dl>
